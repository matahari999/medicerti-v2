export function buildUserPrompt(input: GenerationFormInput): string {
  const hospitalTypeName: Record<string, string> = {
    nursing: '요양병원',
    psychiatric: '정신병원',
    rehabilitation: '재활의료기관',
    acute: '급성기병원',
    tertiary: '상급종합병원',
    general: '종합병원',
    hospital: '병원',
    dental: '치과병원',
    korean: '한방병원',
    other: '기타 병원',
    custom: input.customHospitalType || '직접 입력 병원',
  };

  const typeName = hospitalTypeName[input.hospitalType] || input.hospitalType;
  const typeContext = HOSPITAL_CONTEXT[input.hospitalType] || '';

  return `═══════════════════════════════════════
병원 유형: ${typeName}  ← 이 유형에 맞는 문서만 생성할 것
병원명: ${input.hospitalName || '(미입력)'}
병상 수: ${input.beds || '미입력'}
담당 부서: ${input.department || '미입력'}
문서 요청: ${input.userRequest}
═══════════════════════════════════════

${typeContext}

【중요 지시사항】
- 위 병원 유형 컨텍스트에 명시된 직종, 부서명, 용어를 사용하라
- summaryCard.hospitalType 값은 반드시 "${typeName}"으로 기재
- 다른 병원 유형(요양병원·치과병원 등)의 용어가 섞이지 않도록 철저히 구분
- 절차(procedure) 각 항목은 대제목 + 가./나./다. 소항목 구조로 작성
- 체크리스트는 ${typeName}의 실제 업무(감염관리, 안전관리, 기록관리 등)에 맞게 작성

${input.officialSourceText ? `공식 자료 (우선 참고):\n${input.officialSourceText}` : ''}

위 정보를 바탕으로 ${typeName}에 특화된 인증 문서 패키지를 JSON으로 생성하라.`;
}
